function bouncingBall(h, b, w, c = 0) {
  if (h === w && c === 0 || h <= 0 || c > 600 || b >= 1) {
    return -1
  } else {
    if (h > w && c === 0) c = 1
    if (h <= w) return c
    if (h * b > w) c += 2

    return bouncingBall(h * b, b, w, c)
  }
}

